def test_direct_sse_connection():
    """Test direct SSE connection to Agent Zero"""
    print(f"\nğŸ§ Testing Direct SSE Connection...")
    
    try:
        import sseclient
        
        headers = {
            'Accept': 'text/event-stream',
            'Cache-Control': 'no-cache',
            'User-Agent': 'Hibla-Automation-Test/1.0'
        }
        
        response = requests.get(
            'https://ai.innovatehub.ph/mcp/t-0/sse', 
            headers=headers, 
            stream=True, 
            timeout=10
        )
        
        if response.status_code == 200:
            print("âœ… SSE Connection: ESTABLISHED")
            print(f"   ğŸ“¡ Server: https://ai.innovatehub.ph/mcp/t-0/sse")
            print(f"   ğŸ”— Status Code: {response.status_code}")
            print(f"   ğŸ“„ Content Type: {response.headers.get('content-type', 'N/A')}")
            
            # Try to read initial messages (with timeout)
            client = sseclient.SSEClient(response)
            message_count = 0
            
            print("   ğŸ§ Listening for initial messages...")
            start_time = time.time()
            
            try:
                for message in client:
                    if time.time() - start_time > 5:  # 5 second timeout
                        break
                        
                    if message.data:
                        message_count += 1
                        print(f"   ğŸ“¨ Message {message_count}: {message.data[:100]}...")
                        
                        if message_count >= 3:  # Limit to first 3 messages
                            break
            except:
                pass
            
            print(f"   ğŸ“Š Messages received: {message_count}")
            return True
            
        else:
            print(f"âŒ SSE Connection: FAILED ({response.status_code})")
            print(f"   ğŸ“„ Response: {response.text[:200]}...")
            return False
            
    except Exception as e:
        print(f"âŒ SSE Connection: ERROR ({e})")
        return False

